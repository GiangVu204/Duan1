package giangvhph33056.fpoly.duan1.Model;

public class ThuongHieu {
    int MaTH;
    String TenTH;

    public ThuongHieu() {
    }

    public ThuongHieu(int maTH, String tenTH) {
        MaTH = maTH;
        TenTH = tenTH;
    }

    public int getMaTH() {
        return MaTH;
    }

    public void setMaTH(int maTH) {
        MaTH = maTH;
    }

    public String getTenTH() {
        return TenTH;
    }

    public void setTenTH(String tenTH) {
        TenTH = tenTH;
    }
}
